<h1>IV. PERSYARATAN DAN MEKANISME </h1>
PRSYARATAN
Untuk mengusulkan program kerjasama ini, pengusul harus memenuhi beberapa persyaratan.
I. Program KMPI
a) Tim Pengusul Klinik Makalah Publikasi Ilmiah (TPKMPI) berjumlah maksimal 4 (empat) orang.
b) LPPM ITS sebagai perguruan tinggi mitra menyiapkan supervisor dan atau pendamping yang berasal dari rumpun/bidang ilmu sejenis.
c) TPKMPI belum pernah berpengalaman dalam publikasi ilmiah internasional berreputasi.
d) TPKMPI telah memiliki draft makalah yang akan dipublikasikan.
e) Pendanaan yang timbul karena proses supervisi dan atau pendampingan dibebankan kepada TPKMPI.
f) Proses supervisi dan atau pendampingan dilakukan sesuai dengan kesepakatan kedua belah pihak dengan sepengetahuan ketua LPPM ITS.
10
g) TPKMPI wajib mencantumkan supervisor dari ITS pada member author jurnal internasional dan atau prosiding seminar internaional.
h) Biaya publikasi sepenuhnya menjadi tanggung jawab TPKMPI.
II. Program KP
a) Tim Peneliti Pengusul (TPP) terdiri atas ketua dan maksimum 2 anggota, minimal bergelar S2, tidak berstatus mahasiswa dan tidak memegang jabatan struktural, dari kelompok peneliti yang relatif baru dan berkembang dalam kemampuan dan pengalaman melaksanakan penelitian, serta sarana penelitian yang relatif masih terbatas untuk topik penelitian yang diusulkan.
b) Tim Peneliti Mitra (TPM) dalam hal ini ITS terdiri atas maksimal 2 (dua orang), bergelar S3, berasal dari kelompok peneliti, laboratorium, atau pusat studi di ITS dengan track record penelitian dan publikasi yang bermutu yang menunjukkan kepeloporan dalam bidang penelitian yang diusulkan.
c) TPP dan TPM berasal dari perguruan tinggi yang berbeda.
d) Topik penelitian yang diusulkan sebisa mungkin merujuk pada bidang-bidang unggulan ITS yang dijelaskan pada gambar 1 s/d 7, dan diharapkan dapat dilaksanakan dan dikembangkan di TPP.
e) Perombakan personil penelitian dapat dilakukan seiring dengan kebutuhan pengembangan penelitian.
f) Usul penelitian dapat dibuat dalam bentuk multi tahun.
g) Pendanaan Penelitian dapat berasal dari skema penelitian nasional, dana lokal dan internasional.
h) Usulan harus mendapat persetujuan dalam bentuk pernyataan yang ditandatangani oleh ketua TPM yang menyatakan bahwa usulan yang diajukan memang sesuai dengan lingkup keahlian dan bidang penelitian TPM dengan disahkan oleh ketua LPPM-ITS.
i) Kondisi dan kapasitas laboratorium TPM masih memungkinkan untuk menampung TPP selama melakukan penelitian.
j) Luaran penelitian bisa dalam bentuk publikasi ilmiah internasional terindeks dengan mencantumkan kedua institusi yang terlibat, prototype, rekayasa sosial dan HaKi.
k) Penetapan anggaran penelitian ditetapkan berdasarkan keputusan bersama antara TPM dan TPP.
l) Dalam hal TPM sebagai pengusul, ketua peneliti berasal dari ITS sedangkan anggota/member penelitian dapat berasal dari gabungan antara ITS dan perguruan tinggi swasta, dengan pelaksanaan penelitian dilakukan di ITS.
