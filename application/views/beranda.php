<script type="text/javascript">
    function detail_berita(skim_kode, skim_nama, judul, deskripsi, tanggal) {
        $("#skode").hide();
        $("#snama").hide();

        var skimkode = $('#skim_kode');
        var skimnama = $('#skim_nama');
        var judulberita = $('#pengumuman_judul');
        var deskripsiberita = $('#pengumuman_deskripsi');
        var tanggalrilis = $('#pengumuman_tanggal');

        skimkode.empty();
        skimnama.empty();
        judulberita.empty();
        deskripsiberita.empty();
        tanggalrilis.empty();

        if (skim_kode != "") {
            $("#skode").show();
            $("#snama").show();
        }

        skimkode.append(skim_kode);
        skimnama.append(skim_nama);
        judulberita.append(judul);
        deskripsiberita.append(deskripsi);
        tanggalrilis.append(tanggal);
    }
</script>

